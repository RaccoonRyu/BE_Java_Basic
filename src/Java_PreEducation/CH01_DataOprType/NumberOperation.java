package Java_PreEducation.CH01_DataOprType;

public class NumberOperation {
    public static void main(String[] args) {
        // 숫자와 연산 방법

        // 사칙 연산 - 각 연산에 사용되는 기호를 연산자(Operator)라고 한다.
        System.out.println(6+2); // 숫자 더하기 연산. 8
        System.out.println(6-2); // 숫자 빼기 연산. 4
        System.out.println(6*2); // 숫자 곱하기 연산. 12
        System.out.println(6/2); // 숫자 나누기 연산. 3

        // 수학과 관련된 여러 명령어들을 포함하는 Math 라이브러리
        System.out.println(Math.PI); // 원주율을 적당한 정밀도로 표현해주는 Math의 PI
        System.out.println(Math.floor(Math.PI)); // 숫자를 내림하는 Math의 floor. 3.0
        System.out.println(Math.ceil(Math.PI)); // 숫자를 올림하는 Math의 ceil. 4.0
    }
}
