package Java_PreEducation.CH01_DataOprType;

public class StringHandling {
    public static void main(String[] args) {
        // 문자열 다루기

        System.out.println("Hello World".length()); // 문자열의 길이를 계산하는 length() 함수. 11
        System.out.println("Hello, JH ,,, Bye".replace("JH", "KM")); // 문자열 내의 특정 문자열을 원하는 문자열로 변경하는 replace() 함수
    }
}
