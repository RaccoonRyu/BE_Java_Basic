package Java_OriginEducation.CH00_Start;

// HelloJava 클래스
// 파일명과 클래스명은 동일해야한다.
public class HelloJava {
    // main(String[] args) : 프로그램 실행 시작 함수(메서드)
    public static void main(String[] args) {
        // 값을 콘솔에 출력하는 기능
        // Java에서 문자열은 쌍따옴표를 사용
        System.out.println("Hello Java!"); // Java에서 문장의 끝은 세미콜론으로 구분함
    } // 블록을 사용하여 메서드와 시작과 끝을 나타냄

} // 블록을 사용하여 클래스의 시작과 끝을 나타냄
