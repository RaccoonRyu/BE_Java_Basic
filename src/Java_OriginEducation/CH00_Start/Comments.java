package Java_OriginEducation.CH00_Start;

public class Comments {
    public static void main(String[] args) {
        // 주석
        // 한 줄 주석은 슬래시(/) 두개로 표현하고, 이 기호 이후의 모든 텍스트는 주석으로 인식한다.
        // Java가 해당 주석 부분은 무시하고 소스코드를 실행한다.
        // 보통 코드에 대한 설명을 주석으로 적으므로, 사람이 읽기 위해 사용한다.
        System.out.println("Hello Java1");
        //System.out.println("Hello Java2");

        // 여러 줄 주석은 /*, */로 열고 닫는다.
        // 열고 닫은 범위는 Java에서 모두 주석으로 인식한다.
        /*
        System.out.println("Hello Java3");
        System.out.println("Hello Java4");
        */
    }
}
