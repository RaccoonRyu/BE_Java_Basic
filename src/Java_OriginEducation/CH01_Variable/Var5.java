package Java_OriginEducation.CH01_Variable;

public class Var5 {
    public static void main(String[] args) {
        // 변수 선언, 초기화 따로
        int a;
        a = 1;
        System.out.println(a);

        // 변수 선언과 초기화를 함께
        int b = 2;
        System.out.println(b);

        int c = 3, d = 4; // 여러 변수 선언과 초기화를 함께
        System.out.println(c);
        System.out.println(d);
    }
}
