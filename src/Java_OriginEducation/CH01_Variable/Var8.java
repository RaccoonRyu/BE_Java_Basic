package Java_OriginEducation.CH01_Variable;

public class Var8 {
    public static void main(String[] args) {
        // 다양한 숫자 타입의 변수
        // 정수
        byte b = 127; // -128~127
        short s = 32767; // -32768~32767
        int i = 2147483647; // -2147483648 ~ 2147483647 (약 21억). 기본 리터럴
        long l = 9223372036854775807L; //-9,223,372,036,854,775,808 ~ 9,223,372,036,854,775,807. 뒤에 대문자 L 표기 필요

        // 실수
        float f = 10.0f; // 뒤에 소문자 f 표기 필요
        double d = 10.0; // 기본 리터럴

    }
}
