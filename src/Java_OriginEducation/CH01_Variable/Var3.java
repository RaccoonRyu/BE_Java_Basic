package Java_OriginEducation.CH01_Variable;

public class Var3 {
    public static void main(String[] args) {
        int num; // 정수형 변수 선언
        num = 10; // 변수 초기화 a(10)

        System.out.println(num); // 10 출력

        num = 50; // 변수 값 변경 a(10 -> 50). 변수의 값을 변경하면 변수에 들어있던 기존 값은 삭제된다.
        System.out.println(num); // 50 출력
    }
}
